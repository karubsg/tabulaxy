// Fisher-Yates Shuffle Algorithm (Diziyi rastgele karıştırır)
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// --- ANA FONKSİYON ---

export const fetchTabuCards = async (mode: GameMode, previousWords: string[], count: number = 10): Promise<TabuCard[]> => {
  // 1. Mod'a göre ham veriyi seç
  let rawData: TabuCard[] = [];

  if (mode === GameMode.CLASSIC) {
    rawData = [...CLASSIC_POOL];
  } else if (mode === GameMode.SILENT) {
    rawData = SILENT_POOL_STRINGS.map(target => ({
      target,
      forbidden: ["KONUŞMAK YASAK!", "SES ÇIKARMAK YASAK!"],
      category: "Sessiz Sinema"
    }));
  } else if (mode === GameMode.MARATHON) {
    rawData = MARATHON_POOL_STRINGS.map(target => ({
      target,
      forbidden: ["Yasaklı Yok", "Hızlı Ol", "Kelime Say", "Pas Geçme", "Odaklan"], // Maraton modunda yasaklı kelime görünmez ama yapı bozulmasın diye ekledik
      category: "Maraton"
    }));
  }

  // 2. Daha önce kullanılmış kelimeleri çıkar
  const unusedData = rawData.filter(card => !previousWords.includes(card.target));

  // 3. Eğer havuzda yeterince kelime kalmadıysa, tüm havuzu tekrar kullan (Sonsuz döngü için)
  // Kullanıcıya hissettirmeden havuzu resetlemiş oluyoruz.
  const sourceData = unusedData.length < count ? rawData : unusedData;

  // 4. Havuzu karıştır
  const shuffledData = shuffleArray(sourceData);

  // 5. İstenen sayı kadar kelimeyi al ve döndür
  // Simüle edilmiş bir gecikme ekleyelim ki UI 'loading' durumunu göstersin (çok hızlı olursa glitch gibi durabilir)
  await new Promise(resolve => setTimeout(resolve, 300));

  return shuffledData.slice(0, count);
};
